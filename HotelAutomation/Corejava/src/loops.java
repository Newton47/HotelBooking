
public class loops {

	public static void main(String[] args) {

		for(int i=0;i<=10;i++)
		{
			System.out.println(i+ " This is a for loop");
		}
		
		System.out.println(" ");
		int j=1;
		while(j<=10)
		{
			System.out.println(j + " This is a while loop");
			j++;
		}
		
		System.out.println("");
		
		int k=0;
		do
		{
			System.out.println(k+" This is a do-while loop");
			k++;
		}
		while(k<=10);
			
			
			
	}

}
